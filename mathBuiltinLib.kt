fun main() {
    // Math library functions:
    val minimum = Math.min(-15,50)
    println("The minimum among -15 and 50 is : $minimum ")

    val maximum = Math.max(-15,50)
    println("The maximum among -15 and 50 is : $maximum ")

    val round = Math.round(10.7)
    println("The absolute value of 10.7 is : $round ")

    // Math.pow() takes the double value as an input
    val power = Math.pow(2.0,3.0)
    println("The power of (2.0,3.0) i.e 2.0^3.0 is : $power ")


}